/* 
Полнометражная картина
*/

public class Solution {
    public static void main(String[] args) {
        for (int i = 1; i <= 10 ; i++) {
            System.out.print(i+ " ");
        }
        System.out.println();
        for (int j = 2; j <= 20; j++) {
            System.out.print(j + " ");
        }
    }
}